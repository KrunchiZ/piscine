int	ft_check(char *str)
{
	int	i;

	i = 0;
	while (i < 24)
	{
		if (str[i] == '0')
			return (0);
		if (str[i] == '4')
		{
			if (str[i + 8] != '1')
				return (0);
		}
		else if (str[i] == '3')
		{
			if (str[i + 8] != '2' && str[i + 8] != '1')
				return (0);
		}
		else if ((str[i] == '2' && str[i + 8] == '4')
			|| (str[i] == '1' && str[i + 8] == '1'))
			return (0);
		i += 2;
	}
	return (1);
}

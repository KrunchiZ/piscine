#include "ft_stock_str.h"
#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str)
		write(1, str++, 1);
	write(1, "\n", 1);
	return ;
}

void	print_nbr(int nbr)
{
	if (nbr == 0)
		return ;
	print_nbr(nbr / 10);
	nbr = nbr % 10 + '0';
	write(1, &nbr, 1);
	return ;
}

void	ft_putnbr(int nb)
{
	if (nb == 0)
	{
		write(1, "0", 1);
		return ;
	}
	print_nbr(nb);
	write(1, "\n", 1);
	return ;
}

void	ft_show_tab(t_stock_str *par)
{
	int	i;

	i = 0;
	while (par[i].str != 0)
	{
		ft_putstr(par[i].str);
		ft_putnbr(par[i].size);
		ft_putstr(par[i].copy);
		i++;
	}
}
